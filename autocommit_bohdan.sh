#!/bin/bash

# Этап 1: Проверяем, что текущая директория является Git-репозиторием
if [ ! -d ".git" ]; then
    echo "Текущая директория не является репозиторием Git."
    exit 1
fi

# Этап 2: Добавляем все изменения в индекс Git
git add .

# Этап 3: Проверяем, есть ли изменения для коммита
if git diff-index --quiet HEAD; then
    echo "Нет изменений для фиксации."
    exit 0
fi

# Этап 4: Создаём коммит с текущей датой и временем
git commit -m "Автоматический коммит $(date +'%Y-%m-%d %H:%M:%S')"

# Этап 5: Выводим сообщение об успешном выполнении
echo "Изменения успешно зафиксированы."
exit 0